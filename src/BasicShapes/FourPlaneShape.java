package BasicShapes;

public class FourPlaneShape {

    private int mLength;
    private int mWidth;

    public FourPlaneShape(int length, int width) {
        this.mWidth = width;
        this.mLength = length;
    }

    public int getmLength() {
        return mLength;
    }

    public void setmLength(int mLength) {
        this.mLength = mLength;
    }

    public int getmWidth() {
        return mWidth;
    }

    public void setmWidth(int mWidth) {
        this.mWidth = mWidth;
    }
}
